const ListButtonRight = document.getElementById("button-listmenu-right");
const ListButtonLeft = document.getElementById("button-listmenu-left");
const listMenu = document.querySelector(".box-listmenu");


ListButtonRight.addEventListener("click", function () {
    listMenu.classList.add("active")
    ListButtonRight.classList.add("active")
    ListButtonLeft.classList.add("active")
});

ListButtonLeft.addEventListener("click", function () {
    listMenu.classList.remove("active")
    ListButtonRight.classList.remove("active")
    ListButtonLeft.classList.remove("active")
});



